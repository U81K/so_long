#include <stdio.h>

void rec(int n)
{
	if (n > 9)
	{
		rec(n / 10);
		// n = n % 10;
		printf("%d", n % 10);
	}
	if (n <= 9)
		printf("%d", n);
}
int main(void)
{
	rec(100);
	return (0);
}