#include "ft_printf.h"

int	ft_putnbr(long int n)
{
	char	c;
	int		count;

	count = 0;
	if (n < 0)
	{
		count += ft_putchar('-');
		n *= -1;
	}
	if (n > 9)
	{
		count += ft_putnbr(n / 10);
		count += ft_putnbr(n % 10);
	}
	c = n + '0';
	if (n >= 0 && n <= 9)
		count += ft_putchar(c);
	return (count);
}
